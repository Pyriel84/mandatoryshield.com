function applyLang(lang) {
    const t = translations[lang];
    if (!t) return;
    // data-i18n → textContent
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.textContent = t[key];
    });
    // data-i18n-placeholder → setAttribute placeholder
    var inp = document.getElementById('chatbotInput');
    if (inp && t['chat.placeholder']) inp.setAttribute('placeholder', t['chat.placeholder']);
    // data-i18n-suggestion → met à jour data-suggestion
    document.querySelectorAll('[data-i18n-suggestion]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-suggestion');
        if (t[key] !== undefined) el.setAttribute('data-suggestion', t[key]);
    });
    document.documentElement.lang = lang;
    localStorage.setItem('ms_lang', lang);
}
